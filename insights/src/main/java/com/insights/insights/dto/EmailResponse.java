package com.insights.insights.dto;

public class EmailResponse {

    private String rewrittenEmail;
    private boolean sent;
    private String message;

    public EmailResponse() {}

    public EmailResponse(String rewrittenEmail, boolean sent, String message) {
        this.rewrittenEmail = rewrittenEmail;
        this.sent = sent;
        this.message = message;
    }

    public String getRewrittenEmail() { return rewrittenEmail; }
    public void setRewrittenEmail(String rewrittenEmail) { this.rewrittenEmail = rewrittenEmail; }

    public boolean isSent() { return sent; }
    public void setSent(boolean sent) { this.sent = sent; }

    public String getMessage() { return message; }
    public void setMessage(String message) { this.message = message; }
}
